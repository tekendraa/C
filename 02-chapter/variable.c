#include <stdio.h>

int main() {
    int i=10; // Declare and initialize i with 10 
    int j= i; // Declare and initialize j with i
    // %d is called format specifier 
    // %d is for int, %f is for float, %c is for char, %s is for string
    printf("The value of i is %d and value of j is %d\n", i, j);
    return 0;
}